    /**
     * Finds the page number (1-604) for a given surah and ayah.
     */
    fun getPageForAyah(surah: Int, ayah: Int): Int {
        // Binary search or linear search through the pages
        // We look for the largest page index where the starting ayah is <= (surah, ayah)
        for (i in PAGE_STARTS.size - 1 downTo 0) {
            val start = PAGE_STARTS[i]
            if (surah > start.first || (surah == start.first && ayah >= start.second)) {
                return i + 1
            }
        }
        return 1
    }
